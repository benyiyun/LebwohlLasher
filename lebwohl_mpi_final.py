def one_energy(arr,ix,iy,nmax):
    """
    Arguments:
	  arr (float(nmax,nmax)) = array that contains lattice data;
	  ix (int) = x lattice coordinate of cell;
	  iy (int) = y lattice coordinate of cell;
      nmax (int) = side length of square lattice.
    Description:
      Function that computes the energy of a single cell of the
      lattice taking into account periodic boundaries.  Working with
      reduced energy (U/epsilon), equivalent to setting epsilon=1 in
      equation (1) in the project notes.
	Returns:
	  en (float) = reduced energy of cell.
    """
    en = 0.0
    ixp = (ix+1)%nmax # These are the coordinates
    ixm = (ix-1)%nmax # of the neighbours
    iyp = (iy+1)%nmax # with wraparound
    iym = (iy-1)%nmax #
#
# Add together the 4 neighbour contributions
# to the energy
#
    ang = arr[ix,iy]-arr[ixp,iy]
    en += 0.5*(1.0 - 3.0*np.cos(ang)**2)
    ang = arr[ix,iy]-arr[ixm,iy]
    en += 0.5*(1.0 - 3.0*np.cos(ang)**2)
    ang = arr[ix,iy]-arr[ix,iyp]
    en += 0.5*(1.0 - 3.0*np.cos(ang)**2)
    ang = arr[ix,iy]-arr[ix,iym]
    en += 0.5*(1.0 - 3.0*np.cos(ang)**2)
    return en
#=======================================================================
def all_energy(arr,nmax):
    """
    Arguments:
	  arr (float(nmax,nmax)) = array that contains lattice data;
      nmax (int) = side length of square lattice.
    Description:
      Function to compute the energy of the entire lattice. Output
      is in reduced units (U/epsilon).
	Returns:
	  enall (float) = reduced energy of lattice.
    """
    enall = 0.0
    for i in range(nmax):
        for j in range(nmax):
            enall += one_energy(arr,i,j,nmax)
    return enall
#=======================================================================
def get_order(arr,nmax):
    """
    Arguments:
	  arr (float(nmax,nmax)) = array that contains lattice data;
      nmax (int) = side length of square lattice.
    Description:
      Function to calculate the order parameter of a lattice
      using the Q tensor approach, as in equation (3) of the
      project notes.  Function returns S_lattice = max(eigenvalues(Q_ab)).
	Returns:
	  max(eigenvalues(Qab)) (float) = order parameter for lattice.
    """
    Qab = np.zeros((3,3))
    delta = np.eye(3,3)
    #
    # Generate a 3D unit vector for each cell (i,j) and
    # put it in a (3,i,j) array.
    #
    lab = np.vstack((np.cos(arr),np.sin(arr),np.zeros_like(arr))).reshape(3,nmax,nmax)
    for a in range(3):
        for b in range(3):
            for i in range(nmax):
                for j in range(nmax):
                    Qab[a,b] += 3*lab[a,i,j]*lab[b,i,j] - delta[a,b]
    Qab = Qab/(2*nmax*nmax)
    eigenvalues,eigenvectors = np.linalg.eig(Qab)
    return eigenvalues.max()
#=======================================================================
def MC_step_parallel(arr, Ts, nmax, rank, size): # Function adjusted to take rank and size arguments
    scale = 0.1 + Ts
    accept = 0
    local_n = nmax // size  # Divide lattice among processes
    start = rank * local_n
    end = (rank + 1) * local_n if rank != size - 1 else nmax

    xran = np.random.randint(0, high=nmax, size=(local_n, nmax))
    yran = np.random.randint(0, high=nmax, size=(local_n, nmax))
    aran = np.random.normal(scale=scale, size=(local_n, nmax))

    for i in range(local_n):
        for j in range(nmax):
            ix = xran[i, j]
            iy = yran[i, j]
            ang = aran[i, j]
            en0 = one_energy(arr, ix, iy, nmax)
            arr[ix, iy] += ang
            en1 = one_energy(arr, ix, iy, nmax)
            if en1 <= en0:
                accept += 1
            else:
                boltz = np.exp(-(en1 - en0) / Ts)
                if boltz >= np.random.uniform(0.0, 1.0):
                    accept += 1
                else:
                    arr[ix, iy] -= ang
    
    total_accept = MPI.COMM_WORLD.allreduce(accept, op=MPI.SUM) # Accept count is summed across all processes
    return total_accept / (nmax * nmax)
#=======================================================================
def main_parallel(program, nsteps, nmax, temp, pflag):

    comm = MPI.COMM_WORLD # Assign MPI variables
    rank = comm.Get_rank()
    size = comm.Get_size()

    if rank == 0: # Initialise data on master process (0th rank)
        lattice = initdat(nmax)
    else:
        lattice = None
    
    lattice = comm.bcast(lattice, root=0)  # Broadcast lattice to all processes
    energy = np.zeros(nsteps + 1, dtype=np.dtype)
    ratio = np.zeros(nsteps + 1, dtype=np.dtype)
    order = np.zeros(nsteps + 1, dtype=np.dtype)
    
    if rank == 0: # Set initial values on master process
        energy[0] = all_energy(lattice, nmax)
        order[0] = get_order(lattice, nmax)

    comm.Barrier() # Each task blocks until all tasks in the group reach the barrier call
    initial = time.time()
    for it in range(1, nsteps + 1):
        ratio[it] = MC_step_parallel(lattice, temp, nmax, rank, size)
        if rank == 0:
            energy[it] = all_energy(lattice, nmax)
            order[it] = get_order(lattice, nmax)
    final = time.time()
    runtime = final - initial
    
    if rank == 0: # Save output on master process
        print(f"{program}: Size: {nmax}, Steps: {nsteps}, T*: {temp:.3f}: Order: {order[nsteps-1]:.3f}, Time: {runtime:.6f} s")
        savedat(lattice, nsteps, temp, runtime, ratio, energy, order, nmax)
        plotdat(lattice, pflag, nmax)

if __name__ == '__main__':
    comm = MPI.COMM_WORLD # Assign MPI variables again
    rank = comm.Get_rank()
    
    if len(sys.argv) == 5:
        PROGNAME = sys.argv[0]
        ITERATIONS = int(sys.argv[1])
        SIZE = int(sys.argv[2])
        TEMPERATURE = float(sys.argv[3])
        PLOTFLAG = int(sys.argv[4])
        
        main_parallel(PROGNAME, ITERATIONS, SIZE, TEMPERATURE, PLOTFLAG)
    else:
        if rank == 0:
            print("Usage: mpirun -np <num_procs> python {} <ITERATIONS> <SIZE> <TEMPERATURE> <PLOTFLAG>".format(sys.argv[0]))

